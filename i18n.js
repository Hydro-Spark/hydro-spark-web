function changeLanguage(lang) {
    localStorage.setItem('selectedLanguage', lang);
    applyTranslations(lang);
    updateActiveButton(lang);

    // Lógica para cambiar la imagen en solucion.html
    const mainSchematic = document.getElementById('main-schematic');
    if (mainSchematic) {
        if (lang === 'es') mainSchematic.src = 'Esquema_ES.png';
        if (lang === 'val') mainSchematic.src = 'Esquema_VAL.png';
        if (lang === 'en') mainSchematic.src = 'Esquema_EN.png';
    }

    // Lógica para cambiar la imagen de ODS en nosotros.html
    const odsGraphic = document.getElementById('ods-graphic');
    if (odsGraphic) {
        if (lang === 'es' || lang === 'val') {
            odsGraphic.src = 'ODS_ES.png';
        } else if (lang === 'en') {
            odsGraphic.src = 'ODS_EN.png';
        }
    }

    // Lógica para actualizar la gráfica en solucion.html
    if (window.generationChart) {
        window.generationChart.data.datasets[0].label = translations[lang]['chart_hydro'];
        window.generationChart.data.datasets[1].label = translations[lang]['chart_solar'];
        window.generationChart.update();
    }
}

function applyTranslations(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });
}

function updateActiveButton(lang) {
    const buttons = document.querySelectorAll('button[data-lang-btn]');
    buttons.forEach(btn => {
        if (btn.getAttribute('data-lang-btn') === lang) {
            btn.classList.remove('text-gray-500');
            btn.classList.add('text-brand-orange');
            btn.classList.add('font-black');
            btn.classList.remove('font-bold');
        } else {
            btn.classList.remove('text-brand-orange');
            btn.classList.add('text-gray-500');
            btn.classList.add('font-bold');
            btn.classList.remove('font-black');
        }
    });
}

window.onload = () => {
    const savedLang = localStorage.getItem('selectedLanguage') || 'es';
    changeLanguage(savedLang);
};

function showLegal(type) {
    const lang = localStorage.getItem('selectedLanguage') || 'es';
    const titleEl = document.getElementById('legalModalTitle');
    const contentEl = document.getElementById('legalModalContent');
    const modal = document.getElementById('legalModal');
    
    if (titleEl && contentEl && modal) {
        titleEl.innerHTML = translations[lang]['footer_' + type];
        contentEl.innerHTML = translations[lang]['modal_' + type + '_content'];
        modal.classList.remove('hidden');
    }
}

// Cerrar modal al hacer clic fuera
window.addEventListener('click', function(e) {
    const modal = document.getElementById('legalModal');
    if (modal && e.target === modal) {
        modal.classList.add('hidden');
    }
});
